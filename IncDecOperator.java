public class IncDecOperator {
    public static void main(String[] args) {
        int n = 22;

        System.out.println("Start = " + n);
        System.out.println("n++ = " + (n++));
        System.out.println("After n++ = " + n);
        System.out.println("++n = " + (++n));
        System.out.println("n-- = " + (n--));
        System.out.println("--n = " + (--n));
    }
}
